/* CREATE DATABASE

--> There are 2 ways to Create Database in SQL server.

1.) SQL Server Management Studio (Right Click on Database and click New Database)  

(2.) Transact-SQL- Create Database with Transact-SQL: */


CREATE DATABASE MOVIES_Test

/* ALTER DATABASE

The ALTER DATABASE statement must run in an auto-commit mode.
ALTER DATABASE is not allowed in an explicit or implicit transaction.

--> There are 2 ways to Alter Database in SQL server.

1.) SQL Server Management Studio (Right Click on Database and Rename)  

(2.) Transact-SQL- Alter Database with Transact-SQL: */


ALTER DATABASE [TSQLV4]              
MODIFY NAME = [TSQLV5]


/* DELETE DATABASE

There are 2 ways to Delete Database in SQL server.

1.) SQL Server Management Studio (Right Click and Delete Database)

(2.) Transact-SQL - Delete Database with Transact-SQL: */

DROP DATABASE [TSQLV5]
